number = "100"
print(number)
print(type(number))

#converting the string value of number to the integer value
number = int(number)
print(number)
print(type(number))

#converting the integer number to the floting value
number = float(number)
print(number)
print(type(number))

#conveting the number to the complex number
number = complex(number)
print(number)
print(number.real)
print(number.imag)
print(type(number))